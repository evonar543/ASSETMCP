GODOT_SCRIPT = """extends Node2D

var player := Vector2(120, 260)
var enemy := Vector2(780, 260)
var enemy_vx := -120.0
var score := 0.0
var game_over := false

func _process(delta):
    if Input.is_action_just_pressed("restart"):
        get_tree().reload_current_scene()
    if game_over:
        return
    var input := Vector2.ZERO
    input.x = Input.get_axis("move_left", "move_right")
    input.y = Input.get_axis("move_up", "move_down")
    player += input * 220.0 * delta
    enemy.x += enemy_vx * delta
    if enemy.x < 20 or enemy.x > 940:
        enemy_vx *= -1
    score += delta
    if player.distance_to(enemy) < 36:
        game_over = true
    queue_redraw()

func _draw():
    draw_rect(Rect2(Vector2.ZERO, Vector2(960, 540)), Color("#1b2430"))
    draw_circle(player, 16, Color("#6ee7b7"))
    draw_circle(enemy, 20, Color("#f87171"))
    draw_string(ThemeDB.fallback_font, Vector2(20, 32), "Score: %d" % int(score))
    if game_over:
        draw_string(ThemeDB.fallback_font, Vector2(380, 260), "Game Over - restart")
"""
